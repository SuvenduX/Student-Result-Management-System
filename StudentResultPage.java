    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
       
        
         String rollNo =RollNo_TextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement( ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                     
            //Now we are doing the inner join b/w student & result table as we need to display both student details and their marks 
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo='"+rollNo+"' and result.rollNo='"+rollNo+"'");
            if(!rs.first()){  //If the given roll No does not exist
                JOptionPane.showMessageDialog(null, "Entered rollNo doesn't exist");
            }
            else{  //  if exists populate all the data  rs={roll,name,gender,father,course,branch,roll no,physics,maths,chem,elec,bio,total)
                Name_TextField.setText(rs.getString(1));
                Gender_TextField.setText(rs.getString(3));
                  FatherName_TextField.setText(rs.getString(4));
                Course_TextField.setText(rs.getString(5));
                 Branch_TextField.setText(rs.getString(6));
                
                 Physics_TextField.setText(rs.getString(8));
                  Maths_TextField.setText(rs.getString(9));
                Chemistry.setText(rs.getString(10));
                Electrical_TextField.setText(rs.getString(11));
                 Biology_TextField.setText(rs.getString(12));
                TotalMarks_TextField.setText(rs.getString(13));
                //Verdict_TextField.setText(rs.getString(11));
              
               
                 Name_TextField.setEditable(false);
                RollNo_TextField.setEditable(false);
                Gender_TextField.setEditable(false);
                Course_TextField.setEditable(false);
                FatherName_TextField.setEditable(false);
                Branch_TextField.setEditable(false);
                
                 Physics_TextField.setEditable(false);
                 Maths_TextField.setEditable(false);
                Chemistry.setEditable(false);
                Electrical_TextField.setEditable(false);
                TotalMarks_TextField.setEditable(false);
                Verdict_TextField.setEditable(false);
               
               
                
                int Physics = Integer.parseInt(rs.getString(8));
                int Maths = Integer.parseInt(rs.getString(9));
                int Chemistry = Integer.parseInt(rs.getString(10));
                int Electrical = Integer.parseInt(rs.getString(11));
                int Biology = Integer.parseInt(rs.getString(12));
                
                if(Physics<30 || Maths<30 || Chemistry<30 || Electrical<30 || Biology<30){
                    Verdict_TextField.setText("FAIL");
                }
                else{
                   Verdict_TextField.setText("PASS");
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_formComponentShown

